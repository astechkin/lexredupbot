package telegram.api.data;

/**
 *
 * @author user
 */
public class Voice {

    String file_id; //Unique identifier for this file
    Integer duration; //Duration of the audio in seconds as defined by sender
    String mime_type; //Optional. MIME type of the file as defined by sender
    Integer file_size; //Optional. File size

    public String getFile_id() {
        return file_id;
    }

    public void setFile_id(String file_id) {
        this.file_id = file_id;
    }

    public Integer getDuration() {
        return duration;
    }

    public void setDuration(Integer duration) {
        this.duration = duration;
    }

    public String getMime_type() {
        return mime_type;
    }

    public void setMime_type(String mime_type) {
        this.mime_type = mime_type;
    }

    public Integer getFile_size() {
        return file_size;
    }

    public void setFile_size(Integer file_size) {
        this.file_size = file_size;
    }

}
