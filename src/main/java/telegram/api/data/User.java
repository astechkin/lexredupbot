package telegram.api.data;

/**
 *
 * @author user
 */
public class User {

    Integer id; //Unique identifier for this user or bot
    String first_name; //User‘s or bot’s first name
    String last_name; //Optional. User‘s or bot’s last name
    String username; //Optional. User‘s or bot’s username

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getFirst_name() {
        return first_name;
    }

    public void setFirst_name(String first_name) {
        this.first_name = first_name;
    }

    public String getLast_name() {
        return last_name;
    }

    public void setLast_name(String last_name) {
        this.last_name = last_name;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }


}
