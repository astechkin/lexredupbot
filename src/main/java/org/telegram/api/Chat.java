package org.telegram.api;

/**
 *
 * @author user
 */
public class Chat {

    Integer id; //Unique identifier for this chat. This number may be greater than 32 bits and some programming languages may have difficulty/silent defects in interpreting it. But it smaller than 52 bits, so a signed 64 bit integer or double-precision float type are safe for storing this identifier.
    String type; //Type of chat, can be either “private”, “group”, “supergroup” or “channel”
    String title; //Optional. Title, for channels and group chats
    String username; //Optional. Username, for private chats, supergroups and channels if available
    String first_name; //Optional. First name of the other party in a private chat
    String last_name; //Optional. Last name of the other party in a private chat

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getFirst_name() {
        return first_name;
    }

    public void setFirst_name(String first_name) {
        this.first_name = first_name;
    }

    public String getLast_name() {
        return last_name;
    }

    public void setLast_name(String last_name) {
        this.last_name = last_name;
    }

}
