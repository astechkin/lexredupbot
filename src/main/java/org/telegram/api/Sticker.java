package org.telegram.api;

/**
 *
 * @author user
 */
public class Sticker {

    String file_id; //Unique identifier for this file
    Integer width; //Sticker width
    Integer height; //Sticker height
    PhotoSize thumb; //Optional. Sticker thumbnail in .webp or .jpg format
    String emoji; //Optional. Emoji associated with the sticker
    Integer file_size; //Optional. File size

    public String getFile_id() {
        return file_id;
    }

    public void setFile_id(String file_id) {
        this.file_id = file_id;
    }

    public Integer getWidth() {
        return width;
    }

    public void setWidth(Integer width) {
        this.width = width;
    }

    public Integer getHeight() {
        return height;
    }

    public void setHeight(Integer height) {
        this.height = height;
    }

    public PhotoSize getThumb() {
        return thumb;
    }

    public void setThumb(PhotoSize thumb) {
        this.thumb = thumb;
    }

    public String getEmoji() {
        return emoji;
    }

    public void setEmoji(String emoji) {
        this.emoji = emoji;
    }

    public Integer getFile_size() {
        return file_size;
    }

    public void setFile_size(Integer file_size) {
        this.file_size = file_size;
    }

}
